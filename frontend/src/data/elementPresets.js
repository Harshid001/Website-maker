export const normalizeElementType = (type) => elementAliases[type] || elementAliases[String(type || '').replace(/[\s/-]+/g, '')] || type;

export const getElementPreset = (type) => {
  const normalized = normalizeElementType(type);
  return elementPresets[normalized] || elementPresets.card;
};
